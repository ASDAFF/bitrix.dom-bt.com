<h2>Ответы на вопросы по кредитованию: </h2>
<p>
 <strong>Вопрос</strong>: а нужно ли ехать в банк?
</p>
<p>
 <strong>Ответ</strong>: в банк присутствовать обычно нужно, но если вы по каким либо серьезным причинам не можете, то курьер может приехать к Вам, чтобы подписать кредитный договор.
</p>
<hr>
<p>
 <strong>Вопрос</strong>: нужны ли дополнительные справки?
</p>
<p>
 <strong>Ответ</strong>: Собирать множество справок как раньше не надо. Главное – паспорт. Так же надо предоставить на выбор: или водительские права, или загранпаспорт, или свидетельство пенсионного страхования.
</p>
<hr>
 <strong>Вопрос</strong>: какие требования к заемщику?
<p>
 <strong>Ответ</strong>: возраст не менее 21 года, российский паспорт, клиент должен работать на последнем месте работы не менее 3-х месяцев.
</p>
<hr>
<p>
 <strong>Вопрос</strong>: в каких регионах я могу оформить кредит?
</p>
<p>
 <strong>Ответ</strong>: Вы должны жить в Москве, Московской области, Санкт-Петербурге, Ленинградской области. Важна именно прописка.
</p>
<hr>
<p>
 <strong>Вопрос</strong>: в каком банке мне дадут кредит?
</p>
<p>
 <strong>Ответ</strong>: на данный момент мы сотрудничаем с несколькими банками: Райффайзенбанк, Локо Банк. Список банков постоянно расширяется. Возможность получить кредит как минимум в одном из этих банков очень высока.
</p>
<hr>
<p>
 <strong>Вопрос</strong>: Сколько составит переплата при заказе строительства в кредит?
</p>
<p>
 <strong>Ответ</strong>: в зависимости от срока кредита и первоначального взноса переплата составит от 9 до 12%
</p>
<hr>
<p>
 <strong>Вопрос</strong>: на какой срок я могу получить кредит?
</p>
<p>
 <strong>Ответ</strong>: от 6 месяцев до 5лет.
</p>
<hr>
<p>
 <strong>Вопрос</strong>: на какую сумму я могу получить кредит?
</p>
<p>
 <strong>Ответ</strong>: до 2.000.000 рублей
</p>
<hr>
<p>
 <strong>Вопрос</strong>: какой первоначальный взнос?
</p>
<p>
 <strong>Ответ</strong>: по кредиту первоначальный взнос от 0 %. По Вашему желанию он может быть больше.
</p>
<hr>
<p>
 <strong>Вопрос</strong>: а могу я оформить кредит, затем забрать деньги, и не начинать строить дом?
</p>
<p>
 <strong>Ответ</strong>: нет. Банк в случае оформления заявки на сайте предоставляет кредит именно на строительство дома.
</p>
